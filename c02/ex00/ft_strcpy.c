char	*ft_strcpy(char *dest, char *src)
{
	int	b ;

	b = 0 ;
	while (src[b] != '\0')
	{
		dest[b] = src[b];
		b++ ;
	}
	dest[b] = '\0' ;
	return (dest);
}
